package filter;

public class Store {
	final String Product;
	final double value;
	final double rebate;
	final boolean freight;
	
	public Store(String product, double value, double rebate, boolean freight) {
		super();
		Product = product;
		this.value = value;
		this.rebate = rebate;
		this.freight = freight;
	}
	
	

}
